function shuffleArray(array) {
  return array.sort(() => 0.5 - Math.random());
}

function renderReviews(list) {
  const container = document.getElementById("reviewsContainer");
  container.innerHTML = "";

  const selected = list.sort(() => 0.5 - Math.random()).slice(0, 5);

  selected.forEach((item) => {
    const card = document.createElement("div");
    card.className = "card";

    card.innerHTML = `
            <div class="review-text">${item.text}</div>
            <button onclick="copyText(this)">Copy & Post</button>
        `;

    container.appendChild(card);
  });
}

function filterReviews(category, btn) {
  // Save selected category
  localStorage.setItem("activeCategory", category);

  // UI active state
  document
    .querySelectorAll(".filter")
    .forEach((b) => b.classList.remove("active"));
  btn.classList.add("active");

  let filtered =
    category === "all"
      ? reviewList
      : reviewList.filter((r) => r.category === category);

  renderReviews(filtered);
}
document.addEventListener("DOMContentLoaded", () => {
  const savedCategory = localStorage.getItem("activeCategory") || "all";

  // Find matching button
  const btn = document.querySelector(`.filter[onclick*="${savedCategory}"]`);

  if (btn) {
    filterReviews(savedCategory, btn);
  } else {
    renderReviews(reviewList);
  }
});

document.querySelectorAll(".filter").forEach((btn) => {
  btn.addEventListener("click", () => {
    const category = btn.dataset.category;

    localStorage.setItem("activeCategory", category);

    document
      .querySelectorAll(".filter")
      .forEach((b) => b.classList.remove("active"));
    btn.classList.add("active");

    let filtered =
      category === "all"
        ? reviewList
        : reviewList.filter((r) => r.category === category);

    renderReviews(filtered);
  });
});

document.addEventListener("DOMContentLoaded", () => {
  const savedCategory = localStorage.getItem("activeCategory") || "all";

  const btn = document.querySelector(
    `.filter[data-category="${savedCategory}"]`,
  );

  if (btn) btn.click();
});

function copyText(btn) {
  const text = btn.previousElementSibling.innerText;

  navigator.clipboard.writeText(text).then(() => {
    btn.innerText = "Copied ✓";
    setTimeout(() => {
      window.location.href =
        "https://search.google.com/local/writereview?placeid=ChIJB5kuJGLHfKcR6dB3MfAY9g4";
    }, 600);
  });
}

